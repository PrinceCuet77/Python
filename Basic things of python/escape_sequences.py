print("Hello \"Earth\"")                            # Double qoutation escape
print('I\'m Prince')                                # Single qoutation escape
print("This is backslash\\")                        # Backslash escape
print("This is double backslash\\\\")
print("This is 4 backslash\\\\\\\\")                # 4 backslash are written
print(" \\\" \\\' ")                                # Output : \" \'

print("Hel\bllo\n")                                 # New line
print("Hel\\bllo\\n")                               # \b and \n escape

print("Hello \t world")                             # Tab
print("Hello \\t world")                            # Tab escape

# Row string 
print(r"Line A \n Line B")                          # Output shows whatever I write because of raw string 
print(r"Line A \" Line B")